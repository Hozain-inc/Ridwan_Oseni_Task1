# CONDITIONAL STATEMENT
# if statement
age = 20
if age >= 18:
    print("You are eligible to vote")

# if-else statement
wallet = 400
price = 500
if wallet >= price:
    print("Purchase successful")
else:
    print("Insufficient balance")

# if-elif-else
score = 69
if score >= 70:
    print("Grade A")
elif score >= 60:
    print("Grade B")
else:
    print("Grade C")